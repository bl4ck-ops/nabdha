export const getLoveLanguageLabel = (key) => LOVE_LANGUAGES[key]?.label ?? key

export const getRandomSuggestions = (loveLanguageKey, count = 3) => {
  const pool = LOVE_LANGUAGES[loveLanguageKey]?.suggestions ?? []
  if (pool.length === 0) return []
  const shuffled = [...pool].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}
